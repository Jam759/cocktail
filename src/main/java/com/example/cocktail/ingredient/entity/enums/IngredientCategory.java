package com.example.cocktail.ingredient.entity.enums;

public enum IngredientCategory {

    BASE,         // 베이스 주류 (예: 보드카, 럼, 진, 위스키)
    LIQUEUR,      // 리큐어 (예: 블루 큐라소, 칼루아 등)
    SYRUP,        // 시럽류 (예: 설탕 시럽, 그레나딘 등)
    JUICE,        // 과일 주스 (예: 라임 주스, 레몬 주스, 오렌지 주스 등)
    FRUIT,        // 생과일 (예: 라임, 레몬 슬라이스, 체리 등)
    CARBONATED,   // 탄산류 (예: 탄산수, 콜라, 진저에일 등)
    DAIRY,        // 유제품 (예: 우유, 크림 등)
    SPICE,        // 향신료 (예: 민트, 계피, 소금 등)
    ICE,          // 얼음
    OTHER         // 기타

}
